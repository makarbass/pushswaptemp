int	ft_atoi(const char *str)
{
	unsigned long long	k;
	int					t;

	k = 0;
	t = 1;
	while (*str == ' ' || *str == '\f' || *str == '\n'
		|| *str == '\r' || *str == '\t' || *str == '\v')
		str++;
	if (*str == '-')
		t = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		k = k * 10 + *str - 48;
		str++;
	}
	if (k >= 9223372036854775807)
	{
		if (t == 1)
			return (-1);
		return (0);
	}
	return (t * k);
}
